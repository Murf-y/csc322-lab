#include "op_codes.c"

// !! All instruction are 32 bits instructions !!


// *************************** - D Format - Opcode | displacement | Address | Destination/ Source - ************************************************************************
#define LOAD(DestReg, Address, displacement)      (OP_LD << 28) | (displacement << 10) | (Address << 5) | DestReg
#define STORE(SourceReg, Address, displacement)   (OP_LD << 28) | (displacement << 10) | (Address << 5) | SourceReg
// **************************************************************************************************************************************************************


// *************************** - R Format - Opcode | ALUcode | source2 | shiftamnout | source1 | Destination - ****************************************************************************
#define ADD(Reg1,Reg2,Reg3)         (OP_ALU << 28) | (ALU_ADD << 25) | (Reg3 << 20) | (Reg2 << 5) | Reg1
#define SUB(Reg1,Reg2,Reg3)         (OP_ALU << 28) | (ALU_SUB << 25) | (Reg3 << 20) | (Reg2 << 5) | Reg1
#define AND(Reg1,Reg2,Reg3)         (OP_ALU << 28) | (ALU_AND << 25) | (Reg3 << 20) | (Reg2 << 5) | Reg1
#define OR(Reg1,Reg2,Reg3)          (OP_ALU << 28) | (ALU_OR << 25)  | (Reg3 << 20) | (Reg2 << 5) | Reg1
#define XOR(Reg1,Reg2,Reg3)         (OP_ALU << 28) | (ALU_XOR << 25) | (Reg3 << 20) | (Reg2 << 5) | Reg1
#define ADDS(Reg1,Reg2,Reg3)        (OP_ALU << 28) | (ALU_ADDS << 25)| (Reg3 << 20) | (Reg2 << 5) | Reg1
#define SUBS(Reg1,Reg2,Reg3)        (OP_ALU << 28) | (ALU_SUBS << 25)| (Reg3 << 20) | (Reg2 << 5) | Reg1
#define ANDS(Reg1,Reg2,Reg3)        (OP_ALU << 28) | (ALU_ANDS << 25)| (Reg3 << 20) | (Reg2 << 5) | Reg1

#define SL(Reg1,Reg2,ShiftAmount)   (OP_SL <<  28) | (ShiftAmount << 10)| (Reg2 << 5) | Reg1
#define SR(Reg1,Reg2,ShiftAmount)   (OP_SR <<  28) | (ShiftAmount << 10)| (Reg2 << 5) | Reg1
// **************************************************************************************************************************************************************


// ***************************- I Format - **********************************************************************************************************************
#define ADDI(Reg1,Reg2,Immediate)   (OP_ADDI << 28) |(Immediate << 10) | (Reg2 << 5) | Reg1 
#define SUBI(Reg1,Reg2,Immediate)   (OP_SUBI << 28) |(Immediate << 10) | (Reg2 << 5) | Reg1 
// **************************************************************************************************************************************************************


// *************************** - B Format - **********************************************************************************************************************
#define BR(newPC)                   (OP_BR << 28) | newPC
#define BL(newPC)                   (OP_BL << 28) | newPC
// **************************************************************************************************************************************************************


// *************************** - CB Format - **********************************************************************************************************************
#define CBZ(Reg1,newPC)             (OP_CBZ << 28)| (newPC << 5) | Reg1
#define CBNZ(Reg1,newPC)            (OP_CBNZ<< 28)| (newPC << 5) | Reg1
#define BN(newPC)                   (OP_BN <<  28)| (newPC << 5)
#define BZ(newPC)                   (OP_BZ <<  28)| (newPC << 5)
// **************************************************************************************************************************************************************



// ***************************- Return Format - **********************************************************************************************************************
#define RET()                       (OP_RET << 28)
// **************************************************************************************************************************************************************


// ***************************- Halt Format - **********************************************************************************************************************
#define HALT()                      OP_HALT
// **************************************************************************************************************************************************************
